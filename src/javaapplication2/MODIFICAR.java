    private void ModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarActionPerformed
    conector(); // Establecer conexiÃ³n con la base de datos
    String sql = "UPDATE Pokemon SET Raza=?, Peso=?, Hogar=?, Descripcion=? WHERE Nombre=?";
    
    try (PreparedStatement preparedStatement = con.prepareStatement(sql)) {
        // Establecer los valores de los parÃ¡metros de la consulta
        preparedStatement.setString(1, raza.getText());
        preparedStatement.setDouble(2, Double.parseDouble(peso.getText())); // Suponiendo que el peso es un nÃºmero decimal
        preparedStatement.setString(3, hogar.getText());
        preparedStatement.setString(4, description.getText());
        preparedStatement.setString(5, name.getText());
        
        // Ejecutar la consulta de actualizaciÃ³n
        int filasActualizadas = preparedStatement.executeUpdate();
        
        // Comprobar si la actualizaciÃ³n fue exitosa
        if (filasActualizadas > 0) {
            System.out.println("Registro modificado exitosamente");
               JOptionPane.showMessageDialog(rootPane, "Registro modificado exitosamente");
        } else {
            System.out.println("No se pudo modificar el registro");
        }
    } catch (SQLException ex) {
        Logger.getLogger(MODIFICAR.class.getName()).log(Level.SEVERE, null, ex);
    }
    

    }//GEN-LAST:event_ModificarActionPerformed

