    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
         //Insercion
        conector();
        String sql = "INSERT INTO desarrollofisico (idEtapaVida, nombre, edadLimInf, edadLimSup) VALUES (?, ?, ?, ?)";
        try (PreparedStatement preparedStatement = con.prepareStatement(sql)) {
            // Establecer los valores de los par√°metros de la consulta
            preparedStatement.setString(1, IdEstado.getText());
            preparedStatement.setString(2, name.getText());
            preparedStatement.setString(3, edadinf.getText());
            preparedStatement.setString(4, edadsup.getText());

            // Ejecutar la consulta de inserci√≥n
            int filasInsertadas = preparedStatement.executeUpdate();

            // Comprobar si la inserci√≥n fue exitosa
            if (filasInsertadas > 0) {
                System.out.println("Inserci√≥n exitosa");
                JOptionPane.showMessageDialog(rootPane, "Etapa agregada");
            } else {
                System.out.println("No se pudo insertar el registro");
            }
        } catch (SQLException ex) {
            Logger.getLogger(POSTETAPA.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_RegistrarActionPerformed

 
